aux = input("Digite 1 numero: ")
numA = int(aux)

aux = input("Digite 2 numero: ")
numB = int(aux)

soma = numA + numB
produto = numA * numB
divisao = numA // numB
resto = numA % numB

print(numA, "+", numB, "=", soma)
print(numA, "*", numB, "=", produto)
print(numA, "/", numB, "=", divisao)
print(numA, "%", numB, "=", resto)